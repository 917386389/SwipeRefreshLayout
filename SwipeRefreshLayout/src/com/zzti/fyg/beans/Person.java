package com.zzti.fyg.beans;

import java.io.Serializable;

public class Person implements Serializable {
	
	
	private String name;
	private String des;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	
	
	

}
